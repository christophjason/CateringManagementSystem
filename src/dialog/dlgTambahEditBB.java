    public void insertBB(){
        jComboBox1.removeAllItems();
        String SQL = "SELECT * FROM ingredients";
        ResultSet rs = DBConnect.executeQuery(SQL);
        try {
            while(rs.next()){
                jComboBox1.addItem(rs.getString("ingredientsName"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(dlgTambahEditBB.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void insert() throws SQLException{
        String SQL = "SELECT * FROM penerimaaningredients "
                + "INNER JOIN ingredients "
                + "ON ingredients.ingredientsId = penerimaaningredients.ingredientsId "
                + "WHERE id = '"+id+"'";
        ResultSet rs = DBConnect.executeQuery(SQL);
        if(rs.next()){
            jDateChooser1.setDate(rs.getDate(2));
            jComboBox1.setSelectedItem(rs.getString("ingredientsName"));
            jSpinner1.setValue(rs.getInt(4));
            jTextField2.setText(rs.getString(5));
            jTextField3.setText(rs.getString(6));
            jTextArea1.setText(rs.getString(7));
        }
    }

