    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:\
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tgl = sdf.format(jDateChooser1.getDate());
        String ing = jComboBox1.getSelectedItem().toString();
        String qty = jSpinner1.getValue().toString();
        String ppu = jTextField2.getText().toString();
        String namsup = jTextField3.getText().toString();
        String ket = jTextArea1.getText().toString();
        String SQL2 = "SELECT ingredientsId FROM ingredients WHERE ingredientsName = '"+ing+"'";
        ResultSet rs = DBConnect.executeQuery(SQL2);
        int ingredientsId=0;
        try {
            if(rs.next()){
                ingredientsId = Integer.parseInt(rs.getString("ingredientsId"));
                if (tgl == null||ing.isEmpty()||qty == null||ppu.isEmpty()||namsup.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Harap lengkapi data terlebih dahulu"
                            ,"Error",JOptionPane.WARNING_MESSAGE);
                } if(tipe == 0){
                    String SQL = "INSERT INTO penerimaaningredients "
                            + "(tgl,ingredientsId,qty,ppunit,namaSupplier,ket) "
                            + "VALUES ('"+tgl+"','"+ingredientsId+"','"+qty+"','"+ppu+"','"+namsup+"','"+ket+"')";
                    int status = DBConnect.execute(SQL);
                    if(status == 1){
                        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","Success",
                                JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Data gagal ditambahkan","Error",
                                JOptionPane.WARNING_MESSAGE);
                    }
                } else {
                    String SQL3 = "UPDATE penerimaaningredients SET ingredientsId= '"+ingredientsId+"', "
                            + "qty= '"+qty+"', "
                            + "ppunit='"+ppu+"', "
                            + "namaSupplier='"+namsup+"', "
                            + "ket='"+ket+"' "
                            + "WHERE id='"+this.id+"'";
                    int status = DBConnect.execute(SQL3);
                    if (status==1) {
                        JOptionPane.showMessageDialog(this, "Data berhasil dirubah",
                                "Berhasil", JOptionPane.INFORMATION_MESSAGE);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(this, "Data gagal dirubah",
                                "Error", JOptionPane.WARNING_MESSAGE);
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(dlgTambahEditBB.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

