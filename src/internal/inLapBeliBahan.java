    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        int baris = jTable4.getSelectedRow();
        
        if (baris != -1) {
            int id = Integer.parseInt(jTable4.getValueAt(baris, 0).toString());
            String SQL = "DELETE FROM penerimaaningredients WHERE id = '"+id+"'";
            ResultSet status = DBConnect.executeQuery(SQL);
             try {
                 if (status.next()) {
                     jButton5.doClick();
                     JOptionPane.showMessageDialog(this, "Data berhasil dihapus","Success",
                             JOptionPane.INFORMATION_MESSAGE);
                 } else {
                     JOptionPane.showMessageDialog(this, "Data gagal dihapus","Error",JOptionPane.WARNING_MESSAGE);
                 }} catch (SQLException ex) {
                 Logger.getLogger(inLapBeliBahan.class.getName()).log(Level.SEVERE, null, ex);
             }
        } else {
            JOptionPane.showMessageDialog(this, "Harap pilih data terlebih dahulu","Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton17ActionPerformed

