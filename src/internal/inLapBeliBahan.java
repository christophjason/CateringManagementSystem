        public void selectData(){
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tgl = sdf.format(jDateChooser1.getDate());
            String[] kolom = {"ID", "Tanggal","Ingredients ID","Ingredients Name",
                "Qty","Harga","Nama Supplier","Keterangan"};
            DefaultTableModel dtm = new DefaultTableModel(null, kolom);
            String SQL = "SELECT * FROM penerimaaningredients INNER JOIN ingredients "
                + "ON ingredients.ingredientsId = penerimaaningredients.ingredientsID "
                + "WHERE tgl = '"+tgl+"' ";
            ResultSet rs = DBConnect.executeQuery(SQL);
            try {
                while(rs.next()){
                    String id = rs.getString(1);
                    String tgl2 = rs.getString(2);
                    String ingid = rs.getString(3);
                    String ingnam = rs.getString("ingredients.ingredientsName");
                    String qty = rs.getString(4);
                    String ppu = rs.getString(5);
                    String namsup = rs.getString(6);
                    String ket = rs.getString(7);
                    String data[]={id,tgl2,ingid,ingnam,qty,ppu,namsup,ket};
                    dtm.addRow(data);
                }
            } catch (SQLException ex) {
                Logger.getLogger(inLapBeliBahan.class.getName()).log(Level.SEVERE, null, ex);
            }
        jTable4.setModel(dtm);
    }
